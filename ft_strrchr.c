#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char		find;
	size_t		i;

	i = ft_strlen(s);
	find = (char)c;
	while (i > 0)
	{
		if (s[i] == find)
			return ((char *)s + i);
		i--;
	}
	if (s[i] == find)
		return ((char *)s + i);
	return (NULL);
}
